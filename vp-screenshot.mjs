import puppeteer from 'puppeteer';
const url = process.argv[2];
const scrollY = parseInt(process.argv[3] || '0', 10);
const outPath = process.argv[4] || './vp.png';
const h = parseInt(process.argv[5] || '900', 10);
const browser = await puppeteer.launch({ headless: true, args: ['--no-sandbox'] });
const page = await browser.newPage();
await page.setViewport({ width: 1440, height: h, deviceScaleFactor: 2 });
await page.goto(url, { waitUntil: 'networkidle2', timeout: 30000 });
await page.evaluate(() => {
  localStorage.setItem('charm-cookies-accepted', '1');
  const b = document.getElementById('cookie-banner');
  if (b) b.remove();
});
await new Promise(r => setTimeout(r, 600));
await page.evaluate((y) => window.scrollTo(0, y), scrollY);
await new Promise(r => setTimeout(r, 500));
await page.screenshot({ path: outPath, fullPage: false });
await browser.close();
console.log('saved ' + outPath);
